#include <stdio.h>
#include <stdlib.h>

int main() {
    int G[10][10]; // Массив (матрица) размером 10x10
    int n; // Размерность матрицы (количество строк и столбцов)

    // Ввод размерности матрицы
    printf("Введите n:");
    scanf("%d", &n);
    while ((n > 10) || (n <= 0)) {
        // Проверка ввода на корректность
        printf("Введите n:");
        scanf("%d", &n);
    }

    // Ввод элементов матрицы
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < n; j++) {
            scanf("%d", &G[i][j]);
        }
        printf("\n");
    }

    // Вывод введенной матрицы
    printf("Введенная матрица:\n");
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < n; j++) {
            printf("%d\t", G[i][j]);
        }
        printf("\n");
    }

    // Обработка матрицы: сумма отрицательных элементов в каждой строке
    for (int i = 0; i < n; i++) {
        int sum = 0;
        for (int j = 0; j < n; j++) {
            if (G[i][j] < 0) {
                sum = sum + G[i][j];
            }
        }
        // Замена элемента в дополнительной диагонали (n-i-1) на сумму отрицательных элементов
        G[i][n - i - 1] = sum;
        printf("\nСумма отрицательных элементов строки %d: %d", i + 1, sum);
    }
    printf("\n");

    // Вывод измененной матрицы
    printf("Матрица после обработки:\n");
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            printf("%d\t", G[i][j]);
        }
        printf("\n");
    }

    return 0;
}
